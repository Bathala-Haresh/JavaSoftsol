package com.java.Variables;
public class Variables {
    //global variable
    int a=100;
    //float variable
    static float b=12.5f;
    public static void main(String args[]){
        //local variable
        int a=125;
        //typecasting from float to int
        int f=(int)b;
        System.out.println(a);
        System.out.println(f);

    }
}
